package di.uniba.map.type;

/**
 * The ActionType enum represents the types of actions that can be performed in
 * the game.
 */
public enum ActionType {
    /**
     * Represents the action to start the game.
     */
    START,

    /**
     * Represents the action to resume a saved game.
     */
    RESUME,

    /**
     * Represents the action to play/stop music.
     */
    MUSIC,

    /**
     * Represents the action to display a list of commands.
     */
    COMMAND_LIST,

    /**
     * Represents the action to display the game menu.
     */
    MENU,

    /**
     * Represents the action to display the game map.
     */
    MAP,

    /**
     * Represents the action to pick up an item.
     */
    PICKUP,

    /**
     * Represents the action to display the player's inventory.
     */
    INVENTORY,

    /**
     * Represents the action to save the game.
     */
    SAVE,

    /**
     * Represents the action to move north.
     */
    NORTH,

    /**
     * Represents the action to move south.
     */
    SOUTH,

    /**
     * Represents the action to move east.
     */
    EAST,

    /**
     * Represents the action to move west.
     */
    WEST,

    /**
     * Represents the action to talk to a character.
     */
    TALK,

    /**
     * Represents the action to use an item.
     */
    USE,

    /**
     * Represents the action to exit the game.
     */
    EXIT,

    /**
     * Represents the action to shoot.
     */
    SHOOT,

    /**
     * Represents the action to watch something.
     */
    WATCH,

    /**
     * Represents the action to probe something.
     */
    PROBE
}
